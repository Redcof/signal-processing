def generate_batch_images(seed, ps, h, w):
    rng = np.random.default_rng(seed)
    images = []
    images = rng.random((len(ps), h, w))
    images = (images < ps[:, np.newaxis, np.newaxis]).astype(np.float32)
    return images

def single_run_batch(ps: np.ndarray,
               h: int = 700, w: int = 600,
               mm_per_pixel: float = 1.0,
               seed: int = 42,
               device: torch.device = get_device()) -> dict:
    """
    Generate one Bernoulli image per p value, compute all metrics.
    Feature extraction runs as a single batched forward pass on `device`.
    lcf and spanning remain per-image on CPU (scipy cc_label dependency).
    Returns dict of arrays indexed by ps.
    """

    # ── generate all images up front ─────────────────────────────────────────
    imgs_np = generate_batch_images(seed, ps, h, w) # (N, H, W)

    # ── batch feature extraction on device ───────────────────────────────────
    imgs_t  = torch.from_numpy(imgs_np).to(device)          # (N, H, W)
    with TimerContext() as t_batch:
        feats   = extract_features_batch(imgs_t, mm_per_pixel)   # (N, 4*S)
        scores  = clutter_score_batch(feats).cpu().numpy()       # (N,)

    # ── per-image physical metrics (CPU, scipy) ───────────────────────────────
    lcf      = np.array([largest_cluster_fraction(img) for img in imgs_np])
    spanning = np.array([float(spans_image(img))        for img in imgs_np])

    return {
        "scores":   scores,
        "lcf":      lcf,
        "spanning": spanning,
        "time_sec": t_batch.elapsed / len(ps),
    }

def single_run(ps: np.ndarray,
               h: int = 700, w: int = 600,
               mm_per_pixel: float = 1.0,
               seed: int = 42) -> dict:
    """
    Generate one Bernoulli image per p value, compute all metrics.
    Returns dict of arrays indexed by ps.
    """
    rng = np.random.default_rng(seed)
    scores, lcf, spanning = [], [], []
    times = []
    for p in ps:
        img = (rng.random((h, w)) < p).astype(np.float32)
        with TimerContext() as t:
            f   = extract_features(img, mm_per_pixel)
            scores.append(clutter_score(f))
            times.append(t.elapsed)
        lcf.append(largest_cluster_fraction(img))
        spanning.append(float(spans_image(img)))

    return {
        "scores":   np.array(scores),
        "lcf":      np.array(lcf),
        "spanning": np.array(spanning),
        "time_sec": np.mean(times),
    }


