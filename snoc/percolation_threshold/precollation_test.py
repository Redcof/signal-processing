# EXPERIMENT: multi-seed robustness
# ──────────────────────────────────────────────

@timeit
def multi_seed_run(ps: np.ndarray,
                   n_seeds: int = 20,
                   h: int = 700, w: int = 600,
                   mm_per_pixel: float = 1.0) -> dict:
    """
    Run experiment over n_seeds random seeds.
    Returns mean ± std across seeds for each p value.
    """
    all_scores   = []
    all_lcf      = []
    all_spanning = []
    inflections  = []
    peak_grads   = []
    all_times = []
    h, w = IMAGE_SIZE
    print(f"  Running {n_seeds} seeds × {len(ps)} occupancy values …")
    for seed in range(n_seeds):
        # r = single_run(ps, h, w, mm_per_pixel, seed=seed)
        r = single_run_batch(ps, h, w, mm_per_pixel, seed=seed)


        all_scores.append(r["scores"])
        all_lcf.append(r["lcf"])
        all_spanning.append(r["spanning"])
        all_times.append(r["time_sec"])
        inflections.append(find_inflection(r["scores"], ps))
        peak_grads.append(find_peak_derivative(r["scores"], ps))
        if (seed + 1) % 5 == 0:
            print(f"    seed {seed+1}/{n_seeds} done")

    scores_arr = np.array(all_scores)   # (n_seeds, n_ps)
    lcf_arr    = np.array(all_lcf)
    span_arr   = np.array(all_spanning)
    times_arr  = np.array(all_times)
    return {
        "scores_mean":    scores_arr.mean(axis=0),
        "scores_std":     scores_arr.std(axis=0),
        "lcf_mean":       lcf_arr.mean(axis=0),
        "lcf_std":        lcf_arr.std(axis=0),
        "spanning_mean":  span_arr.mean(axis=0),
        "time_sec":       np.mean(times_arr),
        "inflections":    np.array(inflections),
        "peak_grads":     np.array(peak_grads),
    }


