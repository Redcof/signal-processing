    print(f"  pc theory  : {PC_THEORY}")
    print(f"  image size  : {h}x{w}")
    print(f"  mm/px       : {mm_per_pixel}")
    print(f"  seeds       : {n_seeds}")
    print(f"weights {w_density, w_irregularity, w_fragmentation}")
    print("=" * 60)

    # ── compute ───────────────────────────────
    results = None
    if os.path.exists(experiment_file):
        # load precomputed results if available (speeds up iterative development and plotting)
        try:
            with open(experiment_file, 'rb') as f:
                results = pickle.load(f)
                print(f"Loaded precomputed results from \n\t'{experiment_file}'")
        except Exception:
            results = None
    if results is None:
        print(f"Device type: {get_device()}")
        results = multi_seed_run(ps, n_seeds=n_seeds, mm_per_pixel=mm_per_pixel, h=h, w=w)
        # save results for future reuse
        with open(experiment_file, 'wb') as f:
            pickle.dump(results, f)

    scores_mean = results["scores_mean"]
    scores_std  = results["scores_std"]
    lcf_mean    = results["lcf_mean"]
    spanning    = results["spanning_mean"]
    inflections = results["inflections"]
    peak_grads  = results["peak_grads"]

    # ── derivatives (on mean curve) ───────────
    d1 = first_derivative(scores_mean, ps)
    d2 = second_derivative(scores_mean, ps)

    inflection_p  = find_inflection(scores_mean, ps)
    peak_grad_p   = find_peak_derivative(scores_mean, ps)

    # ── print numerical summary ───────────────
    print(f"\nGradient Analysis Results")
    print(f"  Peak of dC/dp (max gradient)        : p = {peak_grad_p:.4f}")
    print(f"  Inflection point (d²C/dp²=0)        : p = {inflection_p:.4f}")
    print(f"  Theoretical pc                     : {PC_THEORY:.4f}")
